package looperfor;

public class ForDemo1 {
    static void main(String[] args) {
        /*
        实现游戏中连续跳跃10次，
        用输出语句模拟跳跃的逻辑

        格式：
            for(初始化；条件判断；条件控制){

            }

            循环三要素
            开始条件
            结束条件
            条件控制

            循环体：重复执行的代码
         */

        for(int n = 1;n <= 10;n++){
            System.out.println("第" + n + "次跳跃");
        }

    }
}
