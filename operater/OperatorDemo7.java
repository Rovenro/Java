package operater;

public class OperatorDemo7 {
    static void main(String[] args) {
        /*
        =       直接赋值
        +=      加后赋值
        -=      减后赋值
        *=      乘后赋值
        /=      除后赋值
        %=      取余后赋值
         */
        int a = 10;
        int b = 20;
        a += b;
        System.out.println(a);
        System.out.println(b);

    }
}
